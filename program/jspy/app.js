const XLSX = require('xlsx');
const dialog = require('node-file-dialog'); // ‡πÉ‡∏ä‡πâ‡∏ï‡∏±‡∏ß‡∏ô‡∏µ‡πâ‡πÄ‡∏õ‡∏¥‡∏î‡∏´‡∏ô‡πâ‡∏≤‡∏ï‡πà‡∏≤‡∏á
const fs = require('fs');

// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå
function loadFile(path) {
    if (!fs.existsSync(path)) throw new Error("‡∏´‡∏≤‡πÑ‡∏ü‡∏•‡πå‡πÑ‡∏°‡πà‡πÄ‡∏à‡∏≠‡∏Ñ‡∏£‡∏±‡∏ö");
    
    const workbook = XLSX.readFile(path);
    const firstSheetName = workbook.SheetNames[0]; // ‡∏≠‡πà‡∏≤‡∏ô Sheet ‡πÅ‡∏£‡∏Å
    return XLSX.utils.sheet_to_json(workbook.Sheets[firstSheetName], { header: 1 });
}


// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏à‡∏±‡∏î‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç (‡∏ï‡∏±‡∏î Text ‡∏ó‡∏¥‡πâ‡∏á ‡πÄ‡∏≠‡∏≤‡πÅ‡∏ï‡πà‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç)
function sortAll(arr) {
    return arr
        .flat() // ‡πÅ‡∏õ‡∏•‡∏á 2D ‡πÄ‡∏õ‡πá‡∏ô 1D
        .filter(item => typeof item === 'number' && !isNaN(item)) // ‡∏Å‡∏£‡∏≠‡∏á‡πÄ‡∏≠‡∏≤‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡∏à‡∏£‡∏¥‡∏á
        .sort((a, b) => a - b); // ‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏ô‡πâ‡∏≠‡∏¢‡πÑ‡∏õ‡∏°‡∏≤‡∏Å
}


